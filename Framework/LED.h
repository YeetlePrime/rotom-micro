#ifndef _FRAMEWORK_LED_H_
#define _FRAMEWORK_LED_H_


#define MODE_BLINKING_ONE_SEC       0b00000001
#define MODE_FLASH_ON_A_PRESS       0b00000010
#define MODE_FLASH_ON_BUTTON_PRESS  0b00000100



#define LED2_MASK 0b00100000
#define LED2_DDR DDRD
#define LED2_PORT PORTD

#define LED1_MASK 0b00000001
#define LED1_DDR DDRB
#define LED1_PORT PORTB

#define LED1_ENABLE_OUTPUT_MODE LED1_DDR |= LED1_MASK
#define LED1_ON LED1_PORT &= ~LED1_MASK
#define LED1_OFF LED1_PORT |= LED1_MASK
#define LED1_TOGGLE LED1_PORT ^= LED1_MASK

#define LED2_ENABLE_OUTPUT_MODE LED2_DDR |= LED2_MASK
#define LED2_ON LED2_PORT &= ~LED2_MASK
#define LED2_OFF LED2_PORT |= LED2_MASK
#define LED2_TOGGLE LED2_PORT ^= LED2_MASK


#endif